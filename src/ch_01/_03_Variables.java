package ch_01;

public class _03_Variables {
    public static void main(String[] args) {
        System.out.println("가나"+"다");
        String name;
        name = "가나";
        System.out.println(name+"다");
        float f = 0.123456789012F; //float 값으로 자동 변환됨
        double d = 0.123456789012;
        System.out.println("float : " + f +", double : " + d);
        int i = 2_147_483_647; // 자동변환 X -2^(-31) ~ 2^(31) 까지 변수값 저장 가능 위는 저장값 초과
        long l = 92_233_720_368_547_758_07L;

        // 참고로 MAX_VALUE 와 MIN_VALUE 는 이렇게 확인 가능
        System.out.println(Long.MAX_VALUE);
        System.out.println(Long.MIN_VALUE);

    }
}

// 2_147_483_647L 이런 식으로 쓰는게 아주 인상깊다. 가독성이 뛰어난 방법인듯 무조건 적용해야지
//종류       설명              저장 공간    값의 범위 (최소값~최대값)
//======================================================================================
//    boolean    논리값            1 bit        true / false
//    --------------------------------------------------------------------------------------
//    byte       부호 있는 정수    8 bits       -128 ~ 127
//    --------------------------------------------------------------------------------------
//    char       유니코드 문자     16 bits      \u0000 ~ \uFFFF
//    --------------------------------------------------------------------------------------
//    short      부호 있는 정수    16 bits      -32768 ~ 32767
//    --------------------------------------------------------------------------------------
//    int        부호 있는 정수    32 bits      -2147483648 ~ 2147483647
//    --------------------------------------------------------------------------------------
//    long       부호 있는 정수    64 bits      -9223372036854775808 ~ 9223372036854775807
//    --------------------------------------------------------------------------------------
//    float      IEEE 754 실수     32 bits      1.40239846E-45f
//    ~ (표현 가능 양수 범위)
//    3.40282347E+38f
//    --------------------------------------------------------------------------------------
//    double     IEEE 754 실수     64 bits      4.94065645841246544E-324
//    ~ (표현 가능 양수 범위)
//    1.79769313486231570E+308
//    ---------------------------------------------------------------------------------